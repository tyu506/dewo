export function phaseLabel(phase: string, lang: UILang): string {
  const zh: Record<string, string> = {
    parse_and_contract: "待绑定工作流生成",
    candidates_and_binding: "节点级模型选择",
    execute_with_binder: "工作流执行",
    graph_validate_and_repair: "图级验收",
    layered_recovery_repair: "分层恢复与修复",
    complete: "已完成",
  };
  const en: Record<string, string> = {
    parse_and_contract: "Workflow pending binding",
    candidates_and_binding: "Per-node model selection",
    execute_with_binder: "Workflow execution",
    graph_validate_and_repair: "Graph-level validation",
    layered_recovery_repair: "Layered recovery & repair",
    complete: "Complete",
  };
  const table = lang === "zh" ? zh : en;
  return table[phase] ?? phase;
}

